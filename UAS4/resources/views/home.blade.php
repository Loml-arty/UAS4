            <b>
                CSS margin mengatur ruang di luar elemen (atas, kanan, bawah, kiri). Penggunaan shorthand margin: top right bottom left; (searah jarum jam) adalah yang paling umum. Contoh: margin: 10px 20px 30px 40px; berarti 10px atas, 20px kanan, 30px bawah, dan 40px kiri. Properti individual seperti margin-top juga tersedia. 
Cara Penulisan CSS Margin:

    Satu Nilai: margin: 10px; (Semua sisi: 10px)
    Dua Nilai: margin: 10px 20px; (Atas/Bawah: 10px, Kiri/Kanan: 20px)
    Tiga Nilai: margin: 10px 20px 30px; (Atas: 10px, Kiri/Kanan: 20px, Bawah: 30px)
    Empat Nilai: margin: 10px 20px 30px 40px; (Atas, Kanan, Bawah, Kiri)
    Individual:
        margin-top
        margin-right
        margin-bottom
        margin-left 

Poin Penting:

    Margin dapat memiliki nilai negatif.
    margin: auto; sering digunakan untuk mengetengahkan elemen secara horizontal.
